public class TalkToPersonExceptions extends Exception {
    
    
 
     public TalkToPersonExceptions() {
     }
 
     public TalkToPersonExceptions(String message) {
         super(message);
     }
 
     public TalkToPersonExceptions(String message, Throwable cause) {
         super(message, cause);
     }
 
     public TalkToPersonExceptions(Throwable cause) {
         super(cause);
     }
 
     public TalkToPersonExceptions(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
         super(message, cause, enableSuppression, writableStackTrace);
     }
     
     
     
 }
    

